	<h3>Parking</h3>
    <p>
		When entering at the main entrance of RIT's campus, head right at the round-about. The third large parking lot on your left is Lot J. Parking in Lot J is not restricted on weekends, so feel free to park in reserved spaces. There are two buildings ahead of this lot, Golisano College of Computing and Informational Sciences is the further one, and is labeled. There will also be BarCamp Rochester signage pointing you in the right direction.
	</p>
	<h3>From I-90 / From the South</h3>
    <p>
		Leave I-90 at the I-390 exit. Follow signs for I-390 northbound. At the fork for I-390 North or Lehigh Station Road. 
		Take the Lehigh Station Road path and turn left onto Lehigh Station Road. Take this road until you come to the T intersection 
		with East River Road. You will pass several RIT entrances (which you can use if you choose, just follow the loop around to lot J). 
		At the intersection with Jefferson Road, turn right. RIT's main entrance will be on your right at the next light.
	</p>
	<h3>From the North</h3>
    <p>
		While taking I-390 southbound, you will want to get off at Scottsville Rd. Make a right onto Scottsville and take that all the way to Jefferson Rd / Ballantyne Rd. 
		The bridge across the river is the key landmark. You will make a left to cross the bridge. RIT's main entrance will be the second light and on the right hand side.
	</p>
