		<h1>BarCamp Rochester</h1>
		<h3>Rochester's Geek Unconference</h3>
		<p>
			
			BarCamp Rochester is an ad-hoc gathering born from the desire for people to share 
			and learn in an open environment. BarCamp Rochester is an intense event with 
			discussions, demos, and interaction from attendees. All attendees should give a 
			demo, a session, or help with one. All presentations are scheduled the day they 
			happen. Prepare in advance, but come early to get a good slot on the schedule. Check-in 
			begins around 8am, with the first presentations starting at 9am!
		</p>
